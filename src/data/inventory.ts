export const searchProducts = (keywords: string[]) => {
  const searchResults = inventory.filter((item) => {
    for (const keyword of keywords) {
      if (item.name.toLowerCase().includes(keyword.toLowerCase())) return item;
    }
  });

  return searchResults;
};
